		shentizhiliang : {
			get: function () {
				let c = this.ruleForm
				let a = c.tizhong/(c.shengao*c.shengao)
				this.ruleForm.shentizhiliang = a?Number(a).toFixed(2):0
				return a?Number(a).toFixed(2):0
			}
			
		},



